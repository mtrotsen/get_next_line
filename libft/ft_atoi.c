#include "libft.h"

int		ft_atoi(const char *str)
{
	long long int	neg;
	long long int	res;
	int				i;

	neg = 1;
	res = 0;
	i = 0;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\n'
		|| str[i] == '\v' || str[i] == '\r' || str[i] == '\f')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			neg = -1;
		i++;
	}
	while (str[i])
	{
		if (str[i] < '0' || '9' < str[i])
			return (res * neg);
		else
			res = (res * 10) + (long long int)(str[i] - '0');
		i++;
	}
	return (res * neg);
}
